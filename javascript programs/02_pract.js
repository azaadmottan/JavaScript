let a = 2
let b = 3

// Arithmetic Operator

console.log("The value of a and b is ",a," ",b)
console.log("a + b",a + b)
console.log("a - b",a - b)
console.log("a * b",a * b)
console.log("a / b",a / b)
console.log("a % b",a % b)
console.log("a ** b",a ** b)
console.log("a++",a++)
console.log("b++",b++)
console.log("++a",++a)
console.log("++b",++b)


// Comparison Operator

console.log("\nThe value of a and b is ",a," ",b)
console.log("a == b",a == b)
console.log("a != b",a != b)
console.log("a > b",a > b)
console.log("a < b",a < b)

